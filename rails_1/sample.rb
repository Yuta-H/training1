class Sample
  #Ruby on rails総合課題
  # 好きな西暦を変数に代入すると、その年に夏季オリンピックが開催されたか分かる


  #好きな西暦をを代入してください
  year = 2020

  #1896以降にオリンピックが開催した
  if year < 1896
    puts "#{year}年にオリンピックはまだありません"

    # 世界情勢により開催されなった年を省く
  elsif year == 1916 || year == 1990  || year == 1944
    puts "#{year}年にオリンピックはまだ開催されていません"

    # オリンピックが開催される年を4の倍数で計算する
  elsif year % 4 == 0
    puts "#{year}年は夏季オリンピックは開催されました"
  else

    #他の年は対象ではないのでそれ以外という括り
    puts "#{year}年は夏季オリンピックイヤーではありません"
  end
end